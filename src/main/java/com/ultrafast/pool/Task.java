package com.ultrafast.pool;

/**
 * Интерфейс для объектов задач, которые могут быть помещены в пул
 */
public interface Task {
    
    /**
     * Уникальный идентификатор задачи
     */
    long getId();
    
    /**
     * Запуск выполнения задачи
     */
    void start();
    
    /**
     * Остановка выполнения задачи
     */
    void stop();
    
    /**
     * Проверка, выполняется ли задача
     */
    boolean isRunning();
    
    /**
     * Проверка, требует ли задача обновления
     */
    boolean needsUpdate();
    
    /**
     * Пометить задачу как требующую обновления
     */
    void markForUpdate();
    
    /**
     * Получить время последнего использования
     */
    long getLastUsedTime();
    
    /**
     * Получить статус задачи в виде строки
     */
    String getStatus();
} 